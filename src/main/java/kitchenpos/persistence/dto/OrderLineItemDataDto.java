package kitchenpos.persistence.dto;

public class OrderLineItemDataDto {

    private Long seq;
    private Long orderId;
    private final Long menuId;
    private final long quantity;

    public OrderLineItemDataDto(final Long seq, final Long orderId, final Long menuId, final long quantity) {
        this.seq = seq;
        this.orderId = orderId;
        this.menuId = menuId;
        this.quantity = quantity;
    }

    public Long getSeq() {
        return seq;
    }

    public Long getOrderId() {
        return orderId;
    }

    public Long getMenuId() {
        return menuId;
    }

    public long getQuantity() {
        return quantity;
    }
}
